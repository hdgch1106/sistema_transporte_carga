    private void jbtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGuardarActionPerformed
        try {
            String mensaje = "";
            CEVehiculo objVehiculoCE = new CEVehiculo();
            //objVehiculoCE.setIdVehiculo(xidVehiculo);
            objVehiculoCE.setPlaca(jtxtPlaca.getText().toUpperCase());
            objVehiculoCE.setNroSerie(jtxtSerie.getText().toUpperCase());
            xidMarca = obtenerIdMarca();
            objVehiculoCE.setMarca(xidMarca);
            xidColor = obtenerIdColor();
            objVehiculoCE.setColor(xidColor);
            xidTipo = obtenerIdTipo();
            objVehiculoCE.setTipoCarga(xidTipo);
            objVehiculoCE.setNroLlantas(Integer.parseInt(jtxtLlantas.getText().toUpperCase()));
            objVehiculoCE.setPeso_bruto(Integer.parseInt(jtxtPBruto.getText().toUpperCase()));
            objVehiculoCE.setPeso_neto(Integer.parseInt(jtxtPNeto.getText().toUpperCase()));
            objVehiculoCE.setPeso_util(Integer.parseInt(jtxtPUtil.getText().toUpperCase()));
            if (sw) {
                objCLVehiculo.insert(objVehiculoCE);
                mensaje = "Registro grabado";
            } else {
                xidVehiculo = Integer.parseInt(jtxtId.getText());
                objVehiculoCE.setIdVehiculo(xidVehiculo);
                objCLVehiculo.update(objVehiculoCE);
                mensaje = "Registro actualizado";
            }
            habilitaControles(false);
            JOptionPane.showMessageDialog(rootPane, mensaje);
            limpiarControles();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jbtnGuardarActionPerformed

    private void jbtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEliminarActionPerformed
        try {
            int op = JOptionPane.showConfirmDialog(rootPane, "¿Está seguro que desea eliminar?", "Pregunta", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (op == JOptionPane.YES_OPTION) {
                CEVehiculo objVehiculoTO = new CEVehiculo();
                objVehiculoTO.setIdVehiculo(Integer.parseInt(jtxtId.getText()));
                objCLVehiculo.delete(objVehiculoTO);
                limpiarControles();
                JOptionPane.showMessageDialog(rootPane, "Registro Borrado");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jbtnEliminarActionPerformed

