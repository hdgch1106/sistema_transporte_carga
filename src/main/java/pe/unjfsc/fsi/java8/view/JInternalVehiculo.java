    private void tablaVehiculoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaVehiculoMouseClicked
        try {
            xidVehiculo = Integer.parseInt(tablaVehiculo.getValueAt(tablaVehiculo.getSelectedRow(), 0).toString());
            rsVehiculo.first();
            do {
                if (xidVehiculo == rsVehiculo.getInt(1)) {
                    jtxtId.setText(String.valueOf(rsVehiculo.getInt(1)));
                    jtxtPlaca.setText((rsVehiculo.getString(2)));
                    jtxtSerie.setText(rsVehiculo.getString(3));
                    jcmbMarca.removeAllItems();
                    jcmbMarca.addItem(rsVehiculo.getString(4));
                    jcmbcolor.removeAllItems();
                    jcmbcolor.addItem(rsVehiculo.getString(5));
                    jcmbCarga.removeAllItems();
                    jcmbCarga.addItem(rsVehiculo.getString(6));
                    jtxtLlantas.setText(String.valueOf(rsVehiculo.getInt(7)));
                    jtxtPBruto.setText(String.valueOf(rsVehiculo.getInt(8)));
                    jtxtPNeto.setText(String.valueOf(rsVehiculo.getInt(9)));
                    jtxtPUtil.setText(String.valueOf(rsVehiculo.getInt(10)));
                    rsVehiculo.last();
                }
            } while (rsVehiculo.next());
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_tablaVehiculoMouseClicked

