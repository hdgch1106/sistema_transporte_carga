    private void jtxtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtBuscarKeyReleased
        try {
            limpiarJTable();
            if (!jtxtBuscar.getText().isEmpty()) {
                rsVehiculo = objCLVehiculo.buscar(jtxtBuscar.getText().trim());
                while (rsVehiculo.next()) {
                    Object registro[] = {rsVehiculo.getInt(1), rsVehiculo.getString(2), rsVehiculo.getString(3),
                        rsVehiculo.getString(4), rsVehiculo.getString(5), rsVehiculo.getString(6), rsVehiculo.getInt(7),
                        rsVehiculo.getInt(8), rsVehiculo.getInt(9), rsVehiculo.getInt(10)};
                    objDtm.addRow(registro);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }    }//GEN-LAST:event_jtxtBuscarKeyReleased
