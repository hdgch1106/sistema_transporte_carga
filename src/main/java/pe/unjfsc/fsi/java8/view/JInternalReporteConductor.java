    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            String cod = jcmbEstado.getSelectedItem().toString();
            if (cod == "ACTIVOS") {
                Connection cn = BDConexion.getInstance().getConection();
                String direccion = System.getProperty("user.dir") + "\\src\\main\\java\\pe\\unjfsc\\fsi\\java8\\reportes\\ConductoresActivos.jrxml";
                JasperReport reporte = JasperCompileManager.compileReport(direccion);
                Map<String, Object> map = new HashMap<String, Object>();
                JasperPrint mostrarReporte = JasperFillManager.fillReport(reporte, map, cn);
                JasperViewer view = new JasperViewer(mostrarReporte, false);
                view.setTitle("Reporte de Envios Aceptados");
                view.setExtendedState(MAXIMIZED_BOTH);
                view.setVisible(true);
            }
            if (cod == "INACTIVOS") {
                Connection cn = BDConexion.getInstance().getConection();
                String direccion = System.getProperty("user.dir") + "\\src\\main\\java\\pe\\unjfsc\\fsi\\java8\\reportes\\ConductoresInactivos.jrxml";
                JasperReport reporte = JasperCompileManager.compileReport(direccion);
                Map<String, Object> map = new HashMap<String, Object>();
                JasperPrint mostrarReporte = JasperFillManager.fillReport(reporte, map, cn);
                JasperViewer view = new JasperViewer(mostrarReporte, false);
                view.setTitle("Reporte de Envios Pendientes");
                view.setExtendedState(MAXIMIZED_BOTH);
                view.setVisible(true);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

