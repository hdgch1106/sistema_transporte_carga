    private void jbtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnBuscarActionPerformed

        String xidestado = jcmbEstado.getSelectedItem().toString();
        try {
            if (xidestado == "ACTIVOS") {
                limpiarJTable();
                rsEstado = objCMRegistro.buscarConductorActivo();
                while (rsEstado.next()) {
                    Object registro[] = {rsEstado.getInt(1), rsEstado.getInt(2), 
                        rsEstado.getString(3), rsEstado.getString(4), rsEstado.getString(5),
                        rsEstado.getInt(6), rsEstado.getInt(7), rsEstado.getString(8), 
                        rsEstado.getInt(9),rsEstado.getString(10)};
                    objDtm.addRow(registro);
                }

            } else if (xidestado == "INACTIVOS") {
                limpiarJTable();
                rsEstado = objCMRegistro.buscarConductorInactivo();
                while (rsEstado.next()) {
                    Object registro[] = {rsEstado.getInt(1), rsEstado.getInt(2), 
                        rsEstado.getString(3), rsEstado.getString(4), rsEstado.getString(5),
                        rsEstado.getInt(6), rsEstado.getInt(7), rsEstado.getString(8), 
                        rsEstado.getInt(9),rsEstado.getString(10)};
                    objDtm.addRow(registro);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR:" + e);
        }
    }//GEN-LAST:event_jbtnBuscarActionPerformed

