    private void jbtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAceptarActionPerformed
        sw = true;
        try {
            String mensaje = "";
            CERegistroAsig objCERegistroAsig = new CERegistroAsig();
            objCERegistroAsig.setIdRegistro(Integer.parseInt(jtxtidRegistro.getText().toUpperCase()));
            java.util.Date fecha = (jDateChooser1.getDate());
            java.sql.Date fechaLlegada = new java.sql.Date(fecha.getTime());
            objCERegistroAsig.setFechaLlegada(fechaLlegada);
            objCMRegistroFinal.set_fecha_llegada(jtxtidRegistro.getText(), sdf.format(fechaLlegada));
            mensaje = "ENVIO FINALIZADO";
            JOptionPane.showMessageDialog(this, mensaje);
            limpiarControles();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
            
        }
    }//GEN-LAST:event_jbtnAceptarActionPerformed

