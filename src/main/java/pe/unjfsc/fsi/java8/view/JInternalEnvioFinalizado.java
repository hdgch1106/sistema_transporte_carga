    private void jbtnMostarEnviosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnMostarEnviosActionPerformed
        limpiarJTable();
        try {
            
            rsRegistro = objCMRegistroFinal.buscar(ABORT);
            while (rsRegistro.next()) {
                Object registro[] = {rsRegistro.getInt(1), rsRegistro.getString(2), rsRegistro.getString(3), rsRegistro.getDouble(4), rsRegistro.getInt(5),
                    rsRegistro.getString(6), rsRegistro.getString(7), rsRegistro.getString(8)};
                objDtm.addRow(registro);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        
        habilitarControles(true);
    }//GEN-LAST:event_jbtnMostarEnviosActionPerformed

